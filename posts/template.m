O pensamento liberal impede-nos de ver quais são as forças que nos impelem a continuar defendendo-o.

Esse é o paradoxo. De dentro desse pensamento, ele acredita que é racional e certo. Precisa sair e se olhar de fora, para enxergar a que classe pertence.


            
  
